package casting_study;

public class ImplicitCasting {

	public static void main(String[] args) {
		//IMPLICIT CASTING :
		//LOWER DATA TYPE >> HIGHER DATA TYPE
		//MEMORY SIZE INCREASING > WIDENING CASTING 
		//NO DATA LOSS
		
		int a=10;
		double b=a;
		
		short p =123;
		double q=p;
		System.out.println(p);
		System.out.println(q);
		
		
	}

}
